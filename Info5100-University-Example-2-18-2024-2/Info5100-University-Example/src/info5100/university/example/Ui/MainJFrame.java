    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        // TODO add your handling code here:
        String un = txtUsername.getText().trim();
    String pw = txtPassword.getText().trim();

    if (un.isEmpty() || pw.isEmpty()) {
        javax.swing.JOptionPane.showMessageDialog(this, "Enter username and password.");
        return;
    }

    // 1) Authenticate against your model
    info5100.university.example.Persona.UserAccount ua =
            userAccountDirectory.IsValidUser(un, pw);
    if (ua == null) {
        javax.swing.JOptionPane.showMessageDialog(this, "Invalid credentials.");
        return;
    }

    // 2) Decide by role (Admin | Faculty | Student | Registrar)
    String role = ua.getRole();
    info5100.university.example.Persona.Person person = ua.getPerson(); // may be null if not linked

    if ("Admin".equalsIgnoreCase(role)) {
    info5100.university.example.Ui.AdminRole.AdminRoleWorkAreaJPanel panel =
        new info5100.university.example.Ui.AdminRole.AdminRoleWorkAreaJPanel(
            personDirectory,
            userAccountDirectory,
            studentDirectory,
            facultyDirectory,
            courseCatalog,
            courseSchedule,
            person,
            workAreajPanel,
            ua // pass current account so MyProfile can populate
        );
    workAreajPanel.removeAll();
    workAreajPanel.add("Admin", panel);
    ((java.awt.CardLayout) workAreajPanel.getLayout()).next(workAreajPanel);
    return;
    }
    
    if ("Registrar".equalsIgnoreCase(role)) {
    info5100.university.example.Ui.RegistrarRole.RegistrarWorkAreaJPanel panel =
        new info5100.university.example.Ui.RegistrarRole.RegistrarWorkAreaJPanel(
            ctx,              // <â€” pass the whole context
            workAreajPanel,   // the card panel
            ua                // current account (for ManageProfile)
        );
    workAreajPanel.removeAll();
    workAreajPanel.add("Registrar", panel);
    ((java.awt.CardLayout) workAreajPanel.getLayout()).next(workAreajPanel);
    return;
}


    /*if ("Registrar".equalsIgnoreCase(role)) {
        // Using your existing Registrar manager panel as the registrar's home for now
        info5100.university.example.Ui.AdminRole.ManageRegistrarJPanel panel =
            new info5100.university.example.Ui.AdminRole.ManageRegistrarJPanel(
                personDirectory,
                workAreajPanel
            );
        workAreajPanel.removeAll();
        workAreajPanel.add("Registrar", panel);
        ((java.awt.CardLayout) workAreajPanel.getLayout()).next(workAreajPanel);
        return;
    }*/
    
    if ("Faculty".equalsIgnoreCase(role)) {
    // person MUST be linked to the UA
    if (person == null) {
        JOptionPane.showMessageDialog(this, "This account has no linked Person.");
        return;
    }

    // Find the faculty profile in the SAME directory used by the UI
    info5100.university.example.Persona.Faculty.FacultyProfile me =
        facultyDirectory.findByUniversityId(person.getUniversityId());

    if (me == null) {
        // If profile not found, create it so panel can work
        me = facultyDirectory.newFacultyProfile(person);
    }

    // Now show the faculty work area (adjust ctor to match your panel)
    info5100.university.example.Ui.FacultyRole.FacultyWorkAreaJPanel panel =
        new info5100.university.example.Ui.FacultyRole.FacultyWorkAreaJPanel(
            ctx,              // UniversityContext (you already build this above)
            me,               // the logged-in faculty profile
            workAreajPanel    // Card container
        );

    workAreajPanel.removeAll();
    workAreajPanel.add("Faculty", panel);
    ((java.awt.CardLayout) workAreajPanel.getLayout()).next(workAreajPanel);
    return;
}

    /*if ("Faculty".equalsIgnoreCase(role)) {
        // Minimal placeholder so app runs without errors until you share FacultyWorkAreaJPanel
        javax.swing.JPanel placeholder = new javax.swing.JPanel();
        placeholder.add(new javax.swing.JLabel("Faculty Work Area - to be wired"));
        workAreajPanel.removeAll();
        workAreajPanel.add("Faculty", placeholder);
        ((java.awt.CardLayout) workAreajPanel.getLayout()).next(workAreajPanel);
        return;
    }*/

    if ("Student".equalsIgnoreCase(role)) {
        // Minimal placeholder so app runs without errors until you share StudentWorkAreaJPanel
        javax.swing.JPanel placeholder = new javax.swing.JPanel();
        placeholder.add(new javax.swing.JLabel("Student Work Area - to be wired"));
        workAreajPanel.removeAll();
        workAreajPanel.add("Student", placeholder);
        ((java.awt.CardLayout) workAreajPanel.getLayout()).next(workAreajPanel);
        return;
    }

    javax.swing.JOptionPane.showMessageDialog(this, "Unknown role: " + role);
    }//GEN-LAST:event_btnLoginActionPerformed

