    private String keyFor(Person p) {
        if (p == null) return (currentAccount != null ? currentAccount.getUserLoginName() : "unknown");
        String uid = p.getUniversityId();
        return (uid != null && !uid.trim().isEmpty()) ? uid : currentAccount.getUserLoginName();
    }

    private void loadProfileIntoFields() {
        if (currentAccount == null) return;
        Person p = currentAccount.getPerson();
        if (p == null) return;

        txtName.setText(safe(p.getName()));
        txtEmail.setText(safe(p.getEmail()));
        txtContactNo.setText(safe(p.getPhone()));

        String oh = officeHoursStore.getOrDefault(keyFor(p), "");
        txtOfficeHours.setText(oh);
    }
    
    

    private static String safe(String s) { return (s == null) ? "" : s; }

    

