    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        if (currentAccount == null) return;
        Person p = currentAccount.getPerson();
        if (p == null) return;

        // Name is usually not changed by registrar; but your UI allows it â€” keep consistent:
        p.setName(txtName.getText().trim());
        p.setEmail(txtEmail.getText().trim());
        p.setPhone(txtContactNo.getText().trim());

        officeHoursStore.put(keyFor(p), txtOfficeHours.getText().trim());

        JOptionPane.showMessageDialog(this, "Profile saved.");
        
        txtName.setEnabled(false);
        txtEmail.setEnabled(false);
        txtContactNo.setEnabled(false);
        txtOfficeHours.setEnabled(false);
        btnSave.setEnabled(false);
    }//GEN-LAST:event_btnSaveActionPerformed

