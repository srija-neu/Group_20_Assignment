    private void populateEnrollmentByCourse() {
        DefaultTableModel dtm = (DefaultTableModel) tblEnrollmentByCourse.getModel();
        dtm.setRowCount(0);

        String deptName = ctx.getDepartment().getName();
        for (Map.Entry<String, CourseSchedule> e : ctx.getAllSchedules().entrySet()) {
            CourseSchedule cs = e.getValue();
            if (cs == null) continue;

            for (CourseOffer co : cs.getCourseOffers()) {
                String cid = co.getCourseNumber();
                String cname = co.getCourseName();
                int enrolled = co.getEnrolledCount();

                dtm.addRow(new Object[]{ deptName, cid, cname, enrolled });
            }
        }
    }

    // ---------------- GPA distribution ----------------

    private void populateGpaDistribution() {
        DefaultTableModel dtm = (DefaultTableModel) tblGpaDistribution.getModel();
        dtm.setRowCount(0);

        StudentDirectory sd = ctx.getDepartment().getStudentDirectory();

        int countHigh = 0;   // >= 3.7
        int countMid  = 0;   // 3.0 - 3.69...
        int countLow  = 0;   // < 3.0

        for (StudentProfile sp : sd.getStudentList()) {
            // GPA = total grade points / total credits
            double points = 0.0;
            int credits = 0;

            ArrayList<SeatAssignment> all = sp.getCourseList();
            for (SeatAssignment sa : all) {
                // GetCourseStudentScore() = credits * grade
                points += sa.GetCourseStudentScore();
                credits += sa.getCreditHours();
            }
            if (credits <= 0) continue; // no GPA yet

            double gpa = points / credits;

            if (gpa >= 3.7) countHigh++;
            else if (gpa >= 3.0) countMid++;
            else countLow++;
        }

        // Your table columns: Program, GPA>=3.7, 3.7>GPA>=3.0, GPA<3.0
        // If you have multiple programs, expand here; for now, single MSIS (dept degree).
        dtm.addRow(new Object[]{
                "MSIS", countHigh, countMid, countLow
        });
    }



