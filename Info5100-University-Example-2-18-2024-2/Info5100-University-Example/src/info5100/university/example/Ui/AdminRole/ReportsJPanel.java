    private void populateUsersByRole() {
        HashMap<String, Integer> counts = new HashMap<String, Integer>();
        ArrayList<UserAccount> list = userAccountDirectory.getUserAccountList();
        for (int i = 0; i < list.size(); i++) {
            String role = list.get(i).getRole();
            if (role == null) role = "Unknown";
            counts.put(role, counts.getOrDefault(role, 0) + 1);
        }
        DefaultTableModel dtm = (DefaultTableModel) tblUsersByRole.getModel();
        dtm.setRowCount(0);
        for (String r : counts.keySet()) {
            dtm.addRow(new Object[]{ r, counts.get(r) });
        }
    }

    private void populateCoursesBySemester() {
        // Example: assume courseSchedule exposes semesters and their course offers
        DefaultTableModel dtm = (DefaultTableModel) tblCoursesBySemester.getModel();
        dtm.setRowCount(0);
        String term = courseSchedule.getSemester();                    // e.g., "Fall 2025"
        java.util.ArrayList<CourseOffer> offers = courseSchedule.getCourseOffers(); // list for this term
        int count = (offers != null) ? offers.size() : 0;

        dtm.addRow(new Object[]{ term, Integer.valueOf(count) });
    }

    private void populateEnrollments() {
        // Enrolled students per course (sum for all terms)
        DefaultTableModel dtm = (DefaultTableModel) tblEnrollments.getModel();
        dtm.setRowCount(0);

        // Since CourseSchedule only has one semester
        String term = courseSchedule.getSemester();
        ArrayList<CourseOffer> offers = courseSchedule.getCourseOffers();
        if (offers == null) return;

        for (int i = 0; i < offers.size(); i++) {
        CourseOffer co = offers.get(i);
        dtm.addRow(new Object[]{
            co.getCourseNumber(),   // Course ID
            co.getCourseName(),     // Course Name (fallbacks to number if name unavailable)
            term,                   // Semester
            co.getEnrolledCount()   // Enrolled Students
        });
    }
    }

    private void populateTuitionRevenue() {
        // Sum paid tuition from Student accounts
        DefaultTableModel dtm = (DefaultTableModel) tblTuitionRevenue.getModel();
        dtm.setRowCount(0);
        // If you track payments per term/course, loop and sum; here we show a simple total:
        double total = 0.0;
        ArrayList<StudentProfile> sp = studentDirectory.getStudentList();
        for (int i = 0; i < sp.size(); i++) {
            if (sp.get(i).getAccount() != null) {
                total += sp.get(i).getAccount().getTotalPaid(); // adjust if you have a different accessor
            }
        }
        dtm.addRow(new Object[]{ "All Terms", Double.valueOf(total) });
    }


