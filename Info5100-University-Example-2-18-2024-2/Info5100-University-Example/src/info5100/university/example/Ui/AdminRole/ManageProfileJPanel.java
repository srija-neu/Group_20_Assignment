private void load() {
    if (currentAdminPerson != null) {
        txtName.setText(currentAdminPerson.getName());
        txtEmail.setText(currentAdminPerson.getEmail());
        txtContactNo.setText(currentAdminPerson.getPhone());
    } else {
        txtName.setText("");
        txtEmail.setText("");
        txtContactNo.setText("");
    }
    if (currentAccount != null) {
        txtUnsername.setText(currentAccount.getUserLoginName());
        txtPassword.setText(currentAccount.getPassword());
    } else {
        txtUnsername.setText("");
        txtPassword.setText("");
    }
}

